.section .text
.globl _start

_start:
    # --------------------------------------------------------------
    # 1. 初始化页表（假设物理内存 16KB，地址 0x0000 - 0x3FFC）
    # --------------------------------------------------------------

    # 根页表基址 = 0x1000 (物理页号 1)
    # 二级页表基址 = 0x2000 (物理页号 2)
    # 目标物理页框 = 0x3000 (物理页号 3)

    # 1.1 设置一级页表项（索引 0，对应 vaddr[31:22] = 0）
    li   t0, 0x1000          # 根页表基址
    li   t1, 0x2000 | 0x01   # 指向二级页表，V=1，R/W/X=0
    sw   t1, 0(t0)           # 存入根页表项

    # 1.2 设置二级页表项（索引 1，对应 vaddr[21:12] = 1）
    li   t2, 0x2000          # 二级页表基址
    # PTE 构造：PPN = 3 (0x3000>>12)，标志位 V=1,R=1,W=1,X=0,U=1
    # 标志位组合：bit0 V, bit1 R, bit2 W, bit3 X, bit4 U → 0b00010111 = 0x17
    li   t3, (3 << 10) | 0x17   # PPN=3 左移10位，加上标志 0x17
    sw   t3, 4(t2)           # 存入二级页表（偏移4字节，即索引1）

    # --------------------------------------------------------------
    # 2. 启用分页
    # --------------------------------------------------------------
    # satp 寄存器： mode=1 (Sv32)，PPN=1 (根页表物理页号)
    li   t4, (1 << 31) | (1 << 22)   # 0x80000000 | 0x00400000 = 0x80400000
    csrw satp, t4

    # 可选：刷新 TLB（对于无 TLB 的设计可忽略）
    sfence.vma

    # --------------------------------------------------------------
    # 3. 测试虚拟地址 0x1000 的读写
    # --------------------------------------------------------------
    li   t5, 0x1000          # 虚拟地址
    li   t6, 0xdeadbeef      # 测试数据
    sw   t6, 0(t5)           # 写入虚拟地址
    fence                    # 确保写入完成
    lw   a0, 0(t5)           # 读回
    li   a1, 0xdeadbeef
    bne  a0, a1, fail

    # 成功：设置通过标志
    li   x28, 0x5A5
    li   x8,  0x5A6
    j done

fail:
    li   x28, 0xDEAD
    li   x8,  0xBEEF

done:
    j done